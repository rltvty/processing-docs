    <div class="intro">

        <p>
            Processing is a flexible software sketchbook and a language for learning how to code within the context of the visual arts. Since 2001, Processing has promoted software literacy within the visual arts and visual literacy within technology. There are tens of thousands of students, artists, designers, researchers, and hobbyists who use Processing for learning and prototyping.
        
            <ul>
            	<li>&raquo; Free to download and open source</li>
            	<li>&raquo; Interactive programs with 2D, 3D or PDF output</li>
            	<li>&raquo; OpenGL integration for accelerated 2D and 3D</li>
            	<li>&raquo; For GNU/Linux, Mac OS X, Windows, Android, and ARM</li>
            	<li>&raquo; Over 100 libraries extend the core software</li>
            	<li>&raquo; Well <a href="./reference/">documented</a>, with many <a href="./books/">books</a> available</li>
            </ul>
            <br />
        </p>
    </div>

    <div class="intro">

        <p>
            <a href="./books/" class="large-link">Books</a><br />
            <br />
            The 2nd edition of <a href="./books/#reasfry2">Getting Started with Processing</a> is here and it's updated for Processing 3. It's now in full color and there's a new chapter on working with data. The 2nd edition of the <a href="./handbook/">Processing Handbook</a> is here too. Every chapter has been revised, and new chapters introduce more ways to work with data and geometry. New “synthesis” chapters offer discussion and worked examples of such topics as sketching with code, modularity, and algorithms.<br />
